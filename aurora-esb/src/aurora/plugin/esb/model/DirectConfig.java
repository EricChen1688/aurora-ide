package aurora.plugin.esb.model;

public class DirectConfig {
	private String name;
	private Router router;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Router getRouter() {
		return router;
	}
	public void setRouter(Router router) {
		this.router = router;
	}

}
